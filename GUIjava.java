    private void verVencedor(){
        String Vencedor = game.verificaVencedor();
        if (Vencedor != ""){
            if (Vencedor == player1.getMarca()){
                DesativaB();
            txtvisor.setText("O Vencedor foi "+player1.getNome());}
            
            else if (Vencedor == player2.getMarca()){
                    DesativaB();
                    txtvisor.setText("O Vencedor foi "+player2.getNome());
                    historico.exibeHistorico();
            }
            
                   }
        }
    
    
    
