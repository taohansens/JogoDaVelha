    public void verificaJogadordaVez(){
        if(player1.isPlayer()&&player2.isPlayer()){
            this.op=1;
            player1.setPlayer(false);
            player2.setPlayer(true);
            txtvisor.setText("Sua vez  "+player2.getNome());
        }else if(player1.isPlayer()&&(player2.isPlayer()==false)){
            this.op=1;
            player1.setPlayer(false);
            player2.setPlayer(true);
            txtvisor.setText("Sua vez "+player2.getNome());
        }else {
            this.op=2;
            player1.setPlayer(true);
            player2.setPlayer(false);
            txtvisor.setText("Sua vez "+player1.getNome());
        }
    }
    
